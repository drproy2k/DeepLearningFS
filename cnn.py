class Convolution:
    def __init__(self, W, b, stride=1, pad=0):
        self.W = W
        self.b = b
        self.stride = stride
        self.pad = pad
        # 중간 데이터（backward 시 사용）
        self.x = None
        self.col = None
        self.col_W = None
        # 가중치와 편향 매개변수의 기울기
        self.dW = None
        self.db = None

    def forward(self, x):
        self.x = x
        FN, C, FH, FW = self.W.shape    # FN : 출력 채널 수, C : 입력 채널 수
        N, C, H, W = x.shape            # N : 입력 데이터 수, C : 입력 채널 수
        self.col = im2col(x, FH, FW, self.stride, self.pad)      # 입력 데이터 펼치기
        self.col_W = self.W.reshape(FN, -1).T                    # 필터 펼치기
        out = np.dot(self.col, self.col_W) + self.b
        out_h = int(1 + (H + 2*self.pad - FH) / self.stride)
        out_w = int(1 + (W + 2*self.pad - FW) / self.stride)
        out = out.reshape(N, out_h, out_w, -1).transpose(0, 3, 1, 2)    # 다차원 배열의 축 순서를 바꾸기 N -1 out_h out_w  -1은 채널수
        return out

    def backward(self, dout):
        FN, C, FH, FW = self.W.shape
        dout = dout.transpose(0,2,3,1).reshape(-1, FN)
        self.db = np.sum(dout, axis=0)
        self.dW = np.dot(self.col.T, dout)
        self.dW = self.dW.transpose(1, 0).reshape(FN, C, FH, FW)
        dcol = np.dot(dout, self.col_W.T)
        dx = col2im(dcol, self.x.shape, FH, FW, self.stride, self.pad)
        return dx


class Pooling:
    def __init__(self, pool_h, pool_w, stride=1, pad=0):
        self.pool_h = pool_h
        self.pool_w = pool_w
        self.stride = stride
        self.pad = pad
        self.x = None
        self.arg_max = None

    def forward(self, x):
        N, C, H, W = x.shape
        out_h = int(1 + (H - self.pool_h) / self.stride)
        out_w = int(1 + (W - self.pool_w) / self.stride)
        col = im2col(x, self.pool_h, self.pool_w, self.stride, self.pad)    # 2차원  N개 x 결과값
        col = col.reshape(-1, self.pool_h*self.pool_w)                      # 상기 결과값을 pool_h x pool_w 크기로 줄인 KN개 2차원 벡터로 변형 (여기서 K는 자른 결과 2조각이면 K = 2)
        arg_max = np.argmax(col, axis=1)
        out = np.max(col, axis=1)       # 행 방향으로 최대값 구하기. (참고로, axis=0이면 열 방향으로 최대값 구하기)
        out = out.reshape(N, out_h, out_w, C).transpose(0, 3, 1, 2)
        self.x = x
        self.arg_max = arg_max
        return out

    def backward(self, dout):
        dout = dout.transpose(0, 2, 3, 1)
        pool_size = self.pool_h * self.pool_w
        dmax = np.zeros((dout.size, pool_size))
        dmax[np.arange(self.arg_max.size), self.arg_max.flatten()] = dout.flatten()
        dmax = dmax.reshape(dout.shape + (pool_size,))
        dcol = dmax.reshape(dmax.shape[0] * dmax.shape[1] * dmax.shape[2], -1)
        dx = col2im(dcol, self.x.shape, self.pool_h, self.pool_w, self.stride, self.pad)
        return dx


class Relu:
    def __init__(self):
        self.mask = None

    def forward(self, x):       # x는 단변수이거나 numpy
        self.mask = (x <= 0)
        out = x.copy()
        out[self.mask] = 0      # 0보다 작은 요소들은 0으로 처리
        return out

    def backward(self, dout):
        dout[self.mask] = 0
        dx = dout
        return dx


